package com.atrosys.model;

public class UniApiRecord {
    private Long uniNationalId;
    private String uniName;
    private Integer uniSubCode;
    private String uniType;
    private String state;
    private String city;
    private String uniStatus;
    private String uniSubStatus;
    private String uniLastEditTime;
    private String uniSubscriptionTime;

    public Long getUniNationalId() {
        return uniNationalId;
    }

    public void setUniNationalId(Long uniNationalId) {
        this.uniNationalId = uniNationalId;
    }

    public String getUniName() {
        return uniName;
    }

    public void setUniName(String uniName) {
        this.uniName = uniName;
    }

    public Integer getUniSubCode() {
        return uniSubCode;
    }

    public void setUniSubCode(Integer uniSubCode) {
        this.uniSubCode = uniSubCode;
    }

    public String getUniType() {
        return uniType;
    }

    public void setUniType(String uniType) {
        this.uniType = uniType;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getUniStatus() {
        return uniStatus;
    }

    public void setUniStatus(String uniStatus) {
        this.uniStatus = uniStatus;
    }

    public String getUniSubStatus() {
        return uniSubStatus;
    }

    public void setUniSubStatus(String uniSubStatus) {
        this.uniSubStatus = uniSubStatus;
    }

    public String getUniLastEditTime() {
        return uniLastEditTime;
    }

    public void setUniLastEditTime(String uniLastEditTime) {
        this.uniLastEditTime = uniLastEditTime;
    }

    public String getUniSubscriptionTime() {
        return uniSubscriptionTime;
    }

    public void setUniSubscriptionTime(String uniSubscriptionTime) {
        this.uniSubscriptionTime = uniSubscriptionTime;
    }
}
